package eugene.com.alias;

public class EndOfTurnTerms {

    private String term;
    private String result;

    public EndOfTurnTerms(String term, String result) {
        this.term = term;
        this.result = result;
    }

    public String getTerm() {

        return term;
    }

    public String getResult() {
        return result;
    }
}
